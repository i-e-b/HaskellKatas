-- A basic experiment with integral interference patterns

{-
 - Waves are emitted from a source toward both the 'plate' and 'subject'.
 - reflections from subject arrive at plate.
 - Difference in phase between direct and reflected wave is summed
 - (each point in the plate receives waves from every unoccluded point
 -  of the subject)
 -
 - In physical plates, this causes sinc concentric circles, each of which
 - interferes with others.
 -
 - shining a similar set of waves on the plate should cause the subject
 - to appear in image.
 -}
